def main() -> None:
    data = json.loads(VOCAB_PATH.read_text(encoding="utf-8"))
    items = data.get("items") or []
    n5_items = [it for it in items if it.get("level") == "N5"][:50]

    for it in n5_items:
        w = it.get("word", "")
        if w not in MANUAL:
            continue
        (ja1, zh1), (ja2, zh2) = MANUAL[w]
        reading = it.get("reading", "")
        it["exampleJa"] = ja1
        it["exampleReading"] = reading
        it["exampleZh"] = zh1
        it["examples"] = [
            {"ja": ja1, "reading": reading, "zh": zh1, "audioPhrase": ""},
            {"ja": ja2, "reading": reading, "zh": zh2, "audioPhrase": ""},
        ]

    VOCAB_PATH.write_text(json.dumps(data, ensure_ascii=False, separators=(",", ":")) + "\n", encoding="utf-8")
    print("Updated N5 first 50 with manual examples.")


if __name__ == "__main__":
    main()

